place = ['shanghai', 'beijing', 'haerbin', 'qingdao']
print("Here is the oringinal list:")
print(place)

#sorted()函数
print("\n\n(sorted())\nHere is another original list:")
print(sorted(place))
print("\nHere is the oringinal list again:")
print(place)
print("\nHere is the reverse order list:")
print(sorted(place, reverse = True))
print("\nHere is the oringinal list again:")
print(place)

#.reverse方法
place = ['shanghai', 'beijing', 'haerbin', 'qingdao']
print("\n\nHere is the reverse order list:")
place.reverse()
print(place)

#.sort()
place = ['shanghai', 'beijing', 'haerbin', 'qingdao']
place.sort()
print("\n\n(.sort())\nHere is the original list:")
print(place)
